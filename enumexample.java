// enum is used to declare constant can be used  inside and outside their class
public class enumexample {
	

	enum mobile
	{
	SAMSUNG ,APPLE ,HTC, MOTOROLA ; //  SEMICOLON IS NOT NECESSARY
		}
	
	public static void main(String[] args) {
		
		
				System.out.println(mobile.SAMSUNG);
				System.out.println(mobile.HTC);
				System.out.println(mobile.APPLE);
				System.out.println(mobile.MOTOROLA);
				System.out.println(mobile.HTC);
				
		
		}

}
